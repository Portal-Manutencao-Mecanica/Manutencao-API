create temporary table user_id_map (
    source_table varchar(30) not null,
    old_id bigint not null,
    new_id bigint not null,
    primary key (source_table, old_id)
);

do $$
declare
    user_row record;
    migrated_user_id bigint;
begin
    for user_row in select id, user_name, user_email, user_created_at from admin loop
        insert into users (
            user_name, user_email, user_password, user_role,
            user_enabled, account_non_locked, created_at, updated_at
        ) values (
            user_row.user_name, user_row.user_email,
            '!MIGRATE_PASSWORD_REQUIRED!', 'ADMIN',
            true, true, coalesce(user_row.user_created_at, current_timestamp),
            coalesce(user_row.user_created_at, current_timestamp)
        ) returning user_id into migrated_user_id;
        insert into user_id_map values ('admin', user_row.id, migrated_user_id);
    end loop;

    for user_row in select id, user_name, user_email, user_created_at from coordinator loop
        insert into users (
            user_name, user_email, user_password, user_role,
            user_enabled, account_non_locked, created_at, updated_at
        ) values (
            user_row.user_name, user_row.user_email,
            '!MIGRATE_PASSWORD_REQUIRED!', 'COORDENADOR',
            true, true, coalesce(user_row.user_created_at, current_timestamp),
            coalesce(user_row.user_created_at, current_timestamp)
        ) returning user_id into migrated_user_id;
        insert into user_id_map values ('coordinator', user_row.id, migrated_user_id);
    end loop;

    for user_row in select id, user_name, user_email, user_created_at from teacher loop
        insert into users (
            user_name, user_email, user_password, user_role,
            user_enabled, account_non_locked, created_at, updated_at
        ) values (
            user_row.user_name, user_row.user_email,
            '!MIGRATE_PASSWORD_REQUIRED!', 'PROFESSOR',
            true, true, coalesce(user_row.user_created_at, current_timestamp),
            coalesce(user_row.user_created_at, current_timestamp)
        ) returning user_id into migrated_user_id;
        insert into user_id_map values ('teacher', user_row.id, migrated_user_id);
    end loop;

    for user_row in select id, user_name, user_email, user_created_at from student loop
        insert into users (
            user_name, user_email, user_password, user_role,
            user_enabled, account_non_locked, created_at, updated_at
        ) values (
            user_row.user_name, user_row.user_email,
            '!MIGRATE_PASSWORD_REQUIRED!', 'ALUNO',
            true, true, coalesce(user_row.user_created_at, current_timestamp),
            coalesce(user_row.user_created_at, current_timestamp)
        ) returning user_id into migrated_user_id;
        insert into user_id_map values ('student', user_row.id, migrated_user_id);
    end loop;
end
$$;

