create table buy_item (
    buy_item_id bigint generated by default as identity primary key,
    buy_id bigint not null,
    equipment_id bigint not null,
    quantity integer not null,
    technical_specification text,
    sap varchar(100),
    patrimony varchar(100),
    tag varchar(100),
    mechanical_set varchar(150),
    constraint fk_buy_item_buy foreign key (buy_id) references buy(buy_id),
    constraint fk_buy_item_equipment foreign key (equipment_id) references equipment(equipment_id)
);

insert into buy_item (
    buy_id, equipment_id, quantity, technical_specification,
    sap, patrimony, tag, mechanical_set
)
select buy_id, equipment_id, coalesce(buy_quantity, 1),
       buy_technical_specification, buy_sap, buy_patrimony,
       buy_tag, buy_mechanical_set
from buy
where equipment_id is not null;

