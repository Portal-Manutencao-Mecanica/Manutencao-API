create table event (
    event_id bigint generated by default as identity primary key,
    scheduled_action text not null,
    criticality varchar(30) not null,
    created_at timestamp(6) not null,
    scheduled_for timestamp(6) not null,
    requested_at timestamp(6) not null,
    student_id bigint,
    teacher_id bigint not null,
    equipment_id bigint not null,
    machine_id bigint not null,
    place_id bigint not null,
    maintenance_type varchar(30) not null,
    status varchar(30) not null,
    constraint fk_event_student foreign key (student_id) references student(user_id),
    constraint fk_event_teacher foreign key (teacher_id) references teacher(user_id),
    constraint fk_event_equipment foreign key (equipment_id) references equipment(equipment_id),
    constraint fk_event_machine foreign key (machine_id) references machine(machine_id),
    constraint fk_event_place foreign key (place_id) references place(place_id)
);
